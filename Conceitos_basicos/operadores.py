# Operações aritmeticas - matematicos

"""
soma: +
subtração: -
multiplicação: *
divisão: /
divisão inteira: // - descarta a parte decimal
resto da divisão: %
potencia: **

"""
num1 = 10
num2 = 20

print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(num1 / num2)
print(num1 // num2) #sem os numeros dopois da 0,
print(num1 % 2) #resto de 10 dividido por 2
print(num2 ** 3) #20 elevado ao cubo

